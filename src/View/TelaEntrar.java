    public void prosseguir(){
          
          if (setar_campo() >= 0) {
               TelaEvento telaEve = new TelaEvento();
               telaEve.setVisible(true);
               this.setVisible(false);           
            } else {
                   JOptionPane.showMessageDialog(null,"Selecione um evento da tabela.");
            }
        }
        
